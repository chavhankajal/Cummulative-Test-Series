package com.training.pojo;

public class NotesPojo {
	
	
	private int id;
	private String notes_name;
	private String notes_path;
	private String type;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNotes_name() {
		return notes_name;
	}
	public void setNotes_name(String notes_name) {
		this.notes_name = notes_name;
	}
	public String getNotes_path() {
		return notes_path;
	}
	public void setNotes_path(String notes_path) {
		this.notes_path = notes_path;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
	

}
