package com.training.pojo;

public class ResultPojo {
	private int uid;
	private String stud_name;
	private String stud_branch;
	private String stud_subject;
	private int stud_marks;
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getStud_name() {
		return stud_name;
	}
	public void setStud_name(String stud_name) {
		this.stud_name = stud_name;
	}
	public String getStud_branch() {
		return stud_branch;
	}
	public void setStud_branch(String stud_branch) {
		this.stud_branch = stud_branch;
	}
	public String getStud_subject() {
		return stud_subject;
	}
	public void setStud_subject(String stud_subject) {
		this.stud_subject = stud_subject;
	}
	public int getStud_marks() {
		return stud_marks;
	}
	public void setStud_marks(int stud_marks) {
		this.stud_marks = stud_marks;
	}

}
