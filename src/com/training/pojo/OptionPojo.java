package com.training.pojo;

public class OptionPojo {
	
	public int id;
	public long quetion_id;
	public String options;
	public String question_alphabets;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public long getQuetion_id() {
		return quetion_id;
	}
	public void setQuetion_id(long quetion_id) {
		this.quetion_id = quetion_id;
	}
	public String getOptions() {
		return options;
	}
	public void setOptions(String options) {
		this.options = options;
	}
	public String getQuestion_alphabets() {
		return question_alphabets;
	}
	public void setQuestion_alphabets(String question_alphabets) {
		this.question_alphabets = question_alphabets;
	}
	
	

}
